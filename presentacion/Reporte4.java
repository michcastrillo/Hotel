    public void capturardatos(){
        SimpleDateFormat f = new SimpleDateFormat("dd/MM/yyyy");
        Date entradasf = date1.getDate();
        Date salidasf = date2.getDate();
        if (entradasf != null) {
             if (salidasf != null) {
                 ingreso = f.format(entradasf);
                 salida = f.format(salidasf);
                 mostrardatos();
             }else{
                 JOptionPane.showMessageDialog(null, "Seleccione una fecha de salida", "ERROR", JOptionPane.ERROR_MESSAGE);
             }
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione una fecha de ingreso", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
    public void mostrardatos(){
        DefaultTableModel model2 = (DefaultTableModel) jTable1.getModel();
        model2.setRowCount(0);        
        Object[] arreglo2 = new Object[4];
        objTableHabit.listahabitacion= new ArrayList<>();
        ArrayList<objTableHabit> datosH = boreportes.cargarReporte4(ingreso, salida); 
        for (int i = 0; i < datosH.size(); i++) {            
            arreglo2[0] = datosH.get(i).getCodigo();
            arreglo2[1] = datosH.get(i).getPlanta();
            arreglo2[2] = datosH.get(i).getTipo();
            arreglo2[3] = datosH.get(i).getMascota();
            model2.addRow(arreglo2);        
        }
    }
